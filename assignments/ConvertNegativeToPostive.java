package week1.day1.assignments;

public class ConvertNegativeToPostive {
	public static void main(String[] args) {
		// 1. Initialize an integer with a negative number
		// like, int number = -40;
		int number = -40;
		// 2. Check if the number is a negative number
		// Hint : any number that is lesser than zero is a negative number
		if (number < 0) {
			System.out.println("Negative");
		}
		// 3. If so, convert the number to a positive numer
		// Print as below
		// "The number -40 is converted to 40"
		if (number < 0) {
			int postiveNumber = 80;
			System.out.println(number + postiveNumber + " Converted to Postive");
			System.out.println("The number -40 is converted to 40");
		}

	}
}
